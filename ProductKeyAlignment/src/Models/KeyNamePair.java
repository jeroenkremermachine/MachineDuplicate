package Models;

public class KeyNamePair {

		private String keyname1;
		private String keyname2;
		
		 public KeyNamePair(String aaa, String bbb){
		    		keyname1=aaa;
		    		keyname2=bbb;
		 }

		 public String getKeyname1(){
		    return keyname1;
		 }
		 public String getKeyname2(){
		    return keyname2;
		 }
		 

}

